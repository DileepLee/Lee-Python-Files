# using single value in range...

x = range(10)
for a in x:
    print(a)

# Using Dobble values in range...

y = range(10,20) #------>Here 10 is starting Value, 20 is ending Value...
for b in y:
    print(b)

#Using Tripple Values in Range...

z = range(20,30,2) #------>Here 20 is Start value,30 is end value, 2 is Increasing value...  
for c in z:
    print(c)